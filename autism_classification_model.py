df = pd.read_csv("autism_data/data_csv.csv")

# Drop ID column
if "CASE_NO_PATIENT'S" in df.columns:
    df = df.drop(columns=["CASE_NO_PATIENT'S"])

# Target + Features
target_col = "ASD_traits"
