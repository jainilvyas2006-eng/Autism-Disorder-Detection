X = df.drop(columns=[target_col])

# Encode target if categorical
from sklearn.preprocessing import LabelEncoder
if y.dtype == object or not np.issubdtype(y.dtype, np.number):
    le = LabelEncoder()
    y = le.fit_transform(y)

# --------- 2. Controlled Feature Engineering ---------
# Keep only top correlated features (from correlation analysis)
top_features = ['A9','A6','A7','A5','A1','Qchat_10_Score','Global developmental delay/intellectual disability']

X = X[top_features]

# Aggregate score
X['total_score'] = X[['A1','A5','A6','A7','A9']].sum(axis=1)

# Single interaction feature (avoid too many)
X['Qchat_Age_ratio'] = X['Qchat_10_Score'] / (X['total_score'] + 0.1)

